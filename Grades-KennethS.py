GradeLetter = str(input("What is your grade letter(A-F)? "))

if GradeLetter == "A":
    print("The numeric value is 4")
if GradeLetter == "B":
    print("The numeric value is 3")
if GradeLetter == "C":
    print("The numeric value is 2")
if GradeLetter == "D":
    print("The numeric value is 1")
if GradeLetter == "F":
    print("The numeric value is 0")